TEST_P(SpriteLoadingTest, CalculateG1Size)
{
    auto testCase = GetParam();
    rct_g1_element g1{};
    g1.flags = std::get<0>(testCase);
    g1.offset = std::get<1>(testCase).data();
    g1.height = std::get<2>(testCase);
    size_t expected_size = std::get<3>(testCase);
    size_t actual_size = g1_calculate_data_size(&g1);
    ASSERT_EQ(expected_size, actual_size);
}
